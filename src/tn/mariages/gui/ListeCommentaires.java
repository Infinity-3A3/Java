    public void ajouterCom()
    {
         CommentaireDAO c = new CommentaireDAO();
        List<Commentaire> listeCommentaire= new ArrayList<>();
        listeCommentaire = c.DisplayAllCommentairesbyIdProd(id);   
        int i;
        String res="";
        for(i=0;i<listeCommentaire.size();i++)
        {
           int idC = listeCommentaire.get(i).getIdClient();
            System.out.println(idC);
           String nom_client = c.DisplayNameClient(idC).toUpperCase();
            System.out.println(nom_client);
           
            res =  nom_client+"  "+listeCommentaire.get(i).toString() +"\n"+"___________________________________"+"\n"+ res;
        }
        
        texteCom.setText(res); 
    }
    
    
